# -
# *
# /
# % -> resto
# ** -> exponencial

# + 

valor1 = 10
valor2 = 15
soma= valor1 + valor2
print(soma)

# -

valor1 =15
valor2 = 20
subtraçao = valor1 - valor2
print(subtraçao)

# * 

valor1 = 25
valor2 = 25
multiplicaçao = valor1 * valor2
print(multiplicaçao)


# / 
valor1 = 5
valor2 = 2
divisao =valor1 / valor2
print(divisao)

# %

valor1 = 2
valor2 = 4
resto = valor1 % valor2
print(resto)

# **
valor1 = 6
exponencial = valor1 ** 4
